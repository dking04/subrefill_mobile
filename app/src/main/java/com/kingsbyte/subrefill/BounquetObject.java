package com.kingsbyte.subrefill;

public class BounquetObject {
    String name;
    String code;
    String price;
    String monthsPaidFor;
    String invoicePeriod;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getMonthsPaidFor() {
        return monthsPaidFor;
    }

    public void setMonthsPaidFor(String monthsPaidFor) {
        this.monthsPaidFor = monthsPaidFor;
    }

    public String getInvoicePeriod() {
        return invoicePeriod;
    }

    public void setInvoicePeriod(String invoicePeriod) {
        this.invoicePeriod = invoicePeriod;
    }
}
